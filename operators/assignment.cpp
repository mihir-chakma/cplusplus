#include <iostream>
using namespace std;

int main() {

    int num = 10;
    int num1 = 20;

    // += 
    num += 20; // num = num + 20
    cout << num << endl;

    // -+
    num1 -= 10; // num1 = num1 - 10
    cout << num1 << endl;

    // *=
    int num2 = 10;
    num2 *= 5; // num2 = num2 * 5
    cout << num2 << endl;


    // /=
    int num3 = 30;
    num3 /= 6; // num3 = num3 / 5
    cout << num3 << endl;


    // %=
    int num4 = 19;
    num4 %= 4; // num4 = num4 % 4
    cout << num4 << endl;

    return 0;
}